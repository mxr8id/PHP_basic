<?php

/**
 * Лекция 7: Цикл FOR в PHP
 */

// Базовый цикл for
echo "Базовый цикл:\n";
for ($i = 0; $i < 5; $i++) {
    echo "Итерация: $i\n";
}

// Цикл с несколькими переменными
echo "\nДве переменные:\n";
for ($i = 0, $j = 10; $i < 5 && $j > 5; $i++, $j--) {
    echo "i=$i, j=$j\n";
}

// Вложенные циклы
echo "\nТаблица умножения 3x3:\n";
for ($i = 1; $i <= 3; $i++) {
    for ($j = 1; $j <= 3; $j++) {
        echo "$i * $j = " . ($i * $j) . "\n";
    }
}

// continue - пропуск чётных
echo "\nНечётные 0-9:\n";
for ($i = 0; $i < 10; $i++) {
    if ($i % 2 == 0) {
        continue;
    }
    echo "Нечетное: $i\n";
}
