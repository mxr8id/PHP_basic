<?php

/**
 * Лекция 3: Операторы PHP
 * Математические и строковые операторы
 */

$a = 10;
$b = 3;

// Математические операторы
echo "a + b = " . ($a + $b) . "\n";   // 13
echo "a - b = " . ($a - $b) . "\n";   // 7
echo "a * b = " . ($a * $b) . "\n";   // 30
echo "a / b = " . ($a / $b) . "\n";   // 3.333...
echo "a % b = " . ($a % $b) . "\n";   // 1
echo "a ** b = " . ($a ** $b) . "\n"; // 1000

// Строковые операторы
$str1 = "Hello";
$str2 = "World";
echo $str1 . " " . $str2 . "\n"; // Hello World

// Математические функции
echo "abs(-5) = " . abs(-5) . "\n";           // 5
echo "sqrt(16) = " . sqrt(16) . "\n";         // 4
echo "round(3.14159, 2) = " . round(3.14159, 2) . "\n"; // 3.14
echo "max(1, 5, 3) = " . max(1, 5, 3) . "\n"; // 5
echo "min(1, 5, 3) = " . min(1, 5, 3) . "\n"; // 1
